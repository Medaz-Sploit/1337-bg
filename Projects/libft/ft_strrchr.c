char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	while (s[i])
	{
		if (s[i] == (char) c)
			return(s);
		i--;
	}
	return (0);
}
