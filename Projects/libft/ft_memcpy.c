void	*ft_memcpy(void *dst, void *src, int n)
{
	int		i;
	char	*s1;
	char	*s2;

	i = 0;
	s1 = (char*)dst;
	s2 = (char*)src;
	while (*s2 && i < n)
	{
		s1[i] = s2[i];
		i++;
	}
	return (s1);
}
