int	ft_memcmp(void *s1, void *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (*((unsigned char*)s1 + i) != *((unsigned char*)s2 + i) && i < n)
		i++;
	return ((s1 + i) - (s2 + i));
}
