void	*ft_memset(void *b, int c, int len)
{
	int		i;
	char	*ptr;

	ptr = (char*)b;
	i = 0;
	while (ptr[i] && i < len)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (ptr);
}
