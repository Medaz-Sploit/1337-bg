int	isascii(int c)
{
	unsigned char str;
	str = (unsigned char) c;
	int i;
	int j;

	i= 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	if (str[j] >= 0 && str[j] <= 127)
		j++;
	if (i != j)
		return (0);
	return (1);
}
