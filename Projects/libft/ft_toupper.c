int	ft_toupper(int c)
{
	unsigned char str;
	int i;

	i = 0;
	str = (unsigned char) c;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' &&  str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
