#include <unistd.h>

int main()
{
    int i;
    int j;
    
    j = 5;
    while (j > 0)
    {
        i = j;
        while (i > 0)
        {
            write(1, "#", 1);
            i--;
        }
        write(1, "\n", 1);
        j--;
    }
}