int	ft_str_is_numeric(char *str)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (str[j] != '\0')
		j++;
	while (str[i] >= '0' && str[i] <= '9')
		i++;
	if (i != j)
		return (0);
	return (1);
}
