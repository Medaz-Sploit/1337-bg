int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[j] != '\0' && i < n)
	{
		if (s1[i] != s2[j])
			return (s1[i] - s2[j]);
		i++;
		j++;
	}
	if ((s1[i] != '\0' || s2[j] != '\0') && i < n)
		return (s1[i] - s2[j]);
	return (0);
}
